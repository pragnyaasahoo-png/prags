import numpy as np

a = np.array([1, 2, 3])
b = np.array([4, 5, 6])


print("Addition:", a + b)

print("Subtraction:", a - b)

print("Multiplication:", a * b)

print("Division:", a / b)

print("a raised to power 3:", a ** 3)
print("b raised to power 3:", b ** 3)

print("Square of a:", a ** 2)
print("Square of b:", b ** 2)

print("Mean of a:", np.mean(a))
print("Mean of b:", np.mean(b))